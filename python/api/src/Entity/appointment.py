class Appointment: 
    def __init__(self, id, firstname, lastname, email, begin_date, end_date, business_id, token): 
            self.id = id
            self.firstname = firstname
            self.lastname = lastname
            self.email = email
            self.begin_date = begin_date
            self.end_date = end_date
            self.business_id = business_id
            self.token = token